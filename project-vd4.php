                <p class="projectData">Academic Project&nbsp; &nbsp; &nbsp;| <span>#graphic design</span><span>#illustration</span> <span style= "background-color: #ffffff;">|</span>&nbsp; &nbsp; &nbsp; November 2018</p>
                <h3 class="h3-VD">The Brief</h3>
                <p class="plainText">This academic group assignment required us to select a topic of interest, develop an e-learning site presenting information relevant to the topic broken down into units of content, and a way to test the knowledge of the user before continuing to the next unit.
                My contribution to this project was to define the visual style of the site, as well as to illustrate all the assets needed to present the content clearly.</p>
                <h3 class="h3-VD">The Process</h3>
                <p class="plainText">After deciding on our topic, wine, we started by outlining the content for each unit and defining the visual guidelines that lead the look of each unit. I started by creating a color palette, an illustration style, and a selection of typefaces.</p>
                <figure class="singleImage">
                <img src="images/cru-graphicSt.jpg" alt="Graphic Style for CRU">
                <figcaption> <em>CRU graphic style guidelines</em></figcaption>
                </figure>
                <p class="plainText">As we were working on the content for each unit, I created sets of illustrations related to different aspects of wine such as glassware, tasting tools, and more. These sets worked as building blocks, allowing us to compose images for each section, therefore, ensuring that the content presented was engaging. Not to mention that it helped us to work through the units efficiently and seamlessly.</p>
                <figure class="singleImage">
                <img src="images/cru-illustration.jpg" alt="CRU Illustration Sets">
                <figcaption><em>Illustration sets, created for different wine aspects and used transversely in all units</em></figcaption>
                </figure>
                <h3 class="h3-VD">The Outcome</h3>
                <figure class="singleImage">
                <img src="images/cru-home.gif" alt="CRU Website Home Page">
                <figcaption> <em>Site home page and unit navigation.</em></figcaption>
                </figure>
                <p class="plainText">The final website gathered all the work of content creation, illustration, animation, and coding in one place. In four units, the site presents relevant content about wine, the production process, varietals and tasting, each one with an assessment to test knowledge.
                One of our goals was to communicate the content relying more on images than on text, this challenged me to create engaging sets of illustrations and to use animation on some of them to reinforce the concept. Ultimately, I can say that this was a great project; it certainly presented challenges, but it provided an opportunity to hone my illustration skills, and I feel happy with the outcome.
                </p>
